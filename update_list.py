#update the list using append
li=[1,2,3,4]
print(li)
li.append(4)
print(li)
li.append([3,4,5])
print(li)
#update the list using extend method
li.extend([6,7])
print(li)
#update the list using insert method
li.insert(2,30) #2 is index and 30 is element
print(li)
#update the list using remove method
li1=[10,20,30,40]
li1.remove(10)
print(li1)
#update the list using pop()
li1.pop()
print(li1)